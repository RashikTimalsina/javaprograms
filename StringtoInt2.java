public class StringtoInt2 {
    
    public static void main(String[] args) {

        String str="200";
        int i=Integer.parseInt(str);

        System.out.println(str+100);        // 200100 here  + is a string concatenation operator 
        System.out.println(i+100);          // 200+100 here + is a binary plus operator 

    }

}
