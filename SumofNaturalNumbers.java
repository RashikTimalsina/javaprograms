class  SumofNaturalNumbers
{

    public static void main(String [] args)
    {

        int i;                  //Declaration
        int num=10;
        int sum=0;

        for(i=1;i<=num;++i)
        {

            sum=sum+i;

            System.out.println("Sum of natural numbers between 1 to 10 is :" +sum);

        }

        

    }


}